    /**
     * method : CheckGooglePlayServices
     * Description:check if Google Play Services available or not.GoogleApiAvailability is the Helper class for verifying that the Google Play services APK is available and up-to-date on android device.
     * @return :If result is ConnectionResult.SUCCESS then connection was successful; return true otherwise return false.
     */
    private boolean CheckGooglePlayServices() {
        GoogleApiAvailability googleAPI = GoogleApiAvailability.getInstance();
        int result = googleAPI.isGooglePlayServicesAvailable(this);
        if(result != ConnectionResult.SUCCESS) {
            if(googleAPI.isUserResolvableError(result)) {
                googleAPI.getErrorDialog(this, result,
                        0).show();
            }
            return false;
        }
        return true;
    }


